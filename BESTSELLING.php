    <script>
        function asc(){
            var griditems = jquery(".product_list");

            griditems.sort(function(a,b){
                return(
                    jquery(".label_price",a).data("price") - jquery(".label_price",b).data("price")
                );
            });
                jquery(".shop_product").append(griditems);
        }

        function des(){
            var griditems = jquery(".product_list");

            griditems.sort(function(a,b){
                return(
                    jquery(".label_price",b).data("price") - jquery(".label_price",a).data("price")
                );
            });
                jquery(".shop_product").append(griditems);
        }

        function main(){
            var sorting = jquery("#sort").val();

            if(sorting = "12h"){
                asc();
            }
            elseif(sort = "h21"){
                des();
            }

        }

        jquery("#sort").change(function(){
            main();
        });

